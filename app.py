import streamlit as st

# –í—ã—á–∏—Å–ª–µ–Ω–∏–µ –ù–û–î
# –ò—Å–ø–æ–ª—å–∑—É–µ–º –∞–ª–≥–æ—Ä–∏—Ç–º –ï–≤–∫–ª–∏–¥–∞
def gcd(x, y):
    while y:
        x, y = y, x % y
    return x

st.set_page_config(
    page_title="–ü–µ—Ä–µ—Å–µ—á–µ–Ω–∏—è –¥–∏–∞–≥–æ–Ω–∞–ª–∏ –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–∞ —Å —É–∑–ª–∞–º–∏ —Å–µ—Ç–∫–∏",
    page_icon="üìê",
)

st.title("–ü–µ—Ä–µ—Å–µ—á–µ–Ω–∏—è –¥–∏–∞–≥–æ–Ω–∞–ª–∏ –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–∞ —Å —É–∑–ª–∞–º–∏ —Å–µ—Ç–∫–∏ üìê")
st.write("–î–∞–Ω–Ω–∞—è –ø—Ä–æ–≥—Ä–∞–º–º–∞ –≤—ã—á–∏—Å–ª—è–µ—Ç –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —É–∑–ª–æ–≤ –∫–ª–µ—Ç–æ–∫, —á–µ—Ä–µ–∑ –∫–æ—Ç–æ—Ä—ã–µ –ø—Ä–æ—Ö–æ–¥–∏—Ç –¥–∏–∞–≥–æ–Ω–∞–ª—å –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–∞ —Å –∑–∞–¥–∞–Ω–Ω—ã–º–∏ –¥–ª–∏–Ω–æ–π –∏ —à–∏—Ä–∏–Ω–æ–π.")
st.write("–í–≤–µ–¥–∏—Ç–µ –¥–ª–∏–Ω—É –∏ —à–∏—Ä–∏–Ω—É –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–∞ –≤ –ø–æ–ª—è –Ω–∏–∂–µ –∏ –Ω–∞–∂–º–∏—Ç–µ –∫–Ω–æ–ø–∫—É '–í—ã—á–∏—Å–ª–∏—Ç—å'.")

# –í–≤–æ–¥ –¥–∞–Ω–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º
col1, col2 = st.columns(2)
length = col1.number_input("–í–≤–µ–¥–∏—Ç–µ –¥–ª–∏–Ω—É –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–∞:", value=1, min_value=1, step=1)
width = col2.number_input("–í–≤–µ–¥–∏—Ç–µ —à–∏—Ä–∏–Ω—É –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–∞:", value=1, min_value=1, step=1)

if st.button("–í—ã—á–∏—Å–ª–∏—Ç—å", icon="üìê"):
    result = gcd(int(length), int(width)) - 1
    st.success(f"–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —É–∑–ª–æ–≤ –∫–ª–µ—Ç–æ–∫ –Ω–∞ –¥–∏–∞–≥–æ–Ω–∞–ª–∏ –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–∞: {result}")

